def check_typical_question(question: str) -> str:
    """–ü—Ä–æ–≤–µ—Ä—è–µ—Ç –≤–æ–ø—Ä–æ—Å –Ω–∞ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–µ —Ç–∏–ø–æ–≤—ã–º –ø–∞—Ç—Ç–µ—Ä–Ω–∞–º FAQ"""
    text = question.lower()

    for topic, data in FAQ_PATTERNS.items():
        for pattern in data["patterns"]:
            try:
                if re.search(pattern, text, re.IGNORECASE):
                    logger.info(f"FAQ match found: {topic}")
                    return data["answer"]
            except re.error as e:
                logger.error(f"Regex error in pattern '{pattern}': {e}")
                continue

    return None
